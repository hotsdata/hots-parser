def _directional_rule(
    *,
    hero_name: str,
    ability_catalog_name: str,
    ability_name: str,
    ability_link: int,
    length: float,
    width: float,
    outcome_stat: str,
    impact_delay_gameloops: int = 0,
    shape: str = "line",
    missile_speed: float | None = None,
    max_targets: int | None = None,
    mechanic_length: float | None = None,
    mechanic_width: float | None = None,
    confidence: str = "geometry_proxy_uncalibrated",
) -> SkillshotLandingRule:
    return SkillshotLandingRule(
        hero_name=hero_name,
        ability_catalog_name=ability_catalog_name,
        ability_name=ability_name,
        attempt=AbilityKey(ability_link, 0),
        detector="directional_position_overlap",
        confidence=confidence,
        rule_version="97039.1",
        directional_position=DirectionalPositionRule(
            impact_delay_gameloops=impact_delay_gameloops,
            length=length,
            width=width,
            shape=shape,
            target="enemy_heroes",
            outcome_stat=outcome_stat,
            mechanic_length=mechanic_length,
            mechanic_width=mechanic_width,
            missile_speed=missile_speed,
            max_targets=max_targets,
        ),
        evidence_description=(
            "Counts this ability as landed when one or more enemy Hero units overlap "
            "the configured replay-position %s projected from the caster through the "
            "target point. This is an uncalibrated geometry proxy generated from the "
            "all-hero subagent pass and should be refined with replay-specific stats."
        )
        % shape,
    )


